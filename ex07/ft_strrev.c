char	*ft_strrev(char *str)
{
	int		l;
	int		i;
	char	a;

	l = 0;
	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	while (l < i)
	{
		a = str[i];
		str[i] = str[l];
		str[l] = a;
		i--;
		l++;
	}
	return (str);
}
